
public class VariablesTiposDeDatos {
	// metodo main
	public static void main(String[] args) {
		
		String nombre = "BABH";
		// no se puede cambiar el tipo de dato de las var
		//int edad = 22;
		
		char inicial = 'A';
		// char solo guarda un caracter y usa comillas simples
		
		boolean tienesCrush = false;
		//dato booleano
		
		//tipos de datos enteros
		
		//byte de -128 a 127
		byte edad = 22;
		
		//short
		short suma = 24 + 104;
		
		//int
		int numero = 12334567;
		//long se puede poner _ para separar el numero
		long numeroLargo = 100_000_000;
		
		System.out.println(numeroLargo);
		
		//float
		float promedio = 123.1234f;
		
		//double
		double pi = 3.141624384958;
		
	}
}
